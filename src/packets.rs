fn validate_seed_citations(
    source: &Utf8Path,
    citations: &[String],
) -> Result<CitationValidationReport> {
    let rows = citations
        .iter()
        .map(|citation| validate_seed_citation(source, citation))
        .collect::<Result<Vec<_>>>()?;
    let status = if rows.is_empty() {
        "no_seed_citations"
    } else if rows.iter().all(|row| row.status == "ok") {
        "ok"
    } else {
        "failed"
    };
    Ok(CitationValidationReport {
        status: status.to_string(),
        rows,
    })
}

fn validate_seed_citation(source: &Utf8Path, citation: &str) -> Result<CitationValidationRow> {
    if let Some((table, key)) = parse_fact_citation(citation) {
        let rows = read_fact_rows(source, table)?;
        let resolved = rows.iter().any(|row| {
            row.get("key")
                .and_then(|value| value.as_str())
                .is_some_and(|value| value == key)
        });
        return Ok(CitationValidationRow {
            citation: citation.to_string(),
            status: if resolved { "ok" } else { "missing" }.to_string(),
            detail: if resolved {
                format!("resolved in facts/{table}.jsonl")
            } else {
                format!("missing key `{key}` in facts/{table}.jsonl")
            },
        });
    }
    if let Some((table, key)) = parse_evidence_db_citation(citation) {
        let resolved = evidence_db_fact_exists(source, table, key)?;
        return Ok(CitationValidationRow {
            citation: citation.to_string(),
            status: if resolved { "ok" } else { "missing" }.to_string(),
            detail: if resolved {
                format!("resolved in evidence.db facts table `{table}`")
            } else {
                format!("missing key `{key}` in evidence.db table `{table}`")
            },
        });
    }
    Ok(CitationValidationRow {
        citation: citation.to_string(),
        status: "unsupported".to_string(),
        detail: "expected `facts/<table>.jsonl:<key>` or `evidence.db:<table>:<primary-key>`"
            .to_string(),
    })
}

fn parse_fact_citation(citation: &str) -> Option<(&str, &str)> {
    let rest = citation.strip_prefix("facts/")?;
    let (table, key) = rest.split_once(".jsonl:")?;
    (!table.is_empty() && !key.is_empty()).then_some((table, key))
}

fn parse_evidence_db_citation(citation: &str) -> Option<(&str, &str)> {
    let rest = citation.strip_prefix("evidence.db:")?;
    let (table, key) = rest.split_once(':')?;
    (!table.is_empty() && !key.is_empty()).then_some((table, key))
}

fn evidence_db_fact_exists(source: &Utf8Path, table: &str, key: &str) -> Result<bool> {
    let path = source.join(".c2rust-port/knowledge/evidence.db");
    if !path.exists() {
        return Ok(false);
    }
    let conn = Connection::open(path.as_std_path()).with_context(|| format!("open {path}"))?;
    let count: i64 = conn
        .query_row(
            "SELECT count(*) FROM facts WHERE table_name = ?1 AND key = ?2",
            params![table, key],
            |row| row.get(0),
        )
        .with_context(|| format!("query citation {table}:{key}"))?;
    Ok(count > 0)
}

fn write_citation_validation(root: &Utf8Path, report: &CitationValidationReport) -> Result<()> {
    std::fs::write(
        root.join("CITATION_VALIDATION.json"),
        serde_json::to_string_pretty(report)?,
    )
    .with_context(|| format!("write {}", root.join("CITATION_VALIDATION.json")))?;

    let mut text = String::new();
    text.push_str("# Citation Validation\n\n");
    text.push_str(&format!("- Status: `{}`\n", report.status));
    text.push_str(&format!("- Checked citations: `{}`\n\n", report.rows.len()));
    if report.rows.is_empty() {
        text.push_str("No seed citations were generated for this packet.\n");
    } else {
        text.push_str("| Citation | Status | Detail |\n");
        text.push_str("| --- | --- | --- |\n");
        for row in &report.rows {
            text.push_str(&format!(
                "| `{}` | `{}` | {} |\n",
                markdown_table_cell(&row.citation),
                row.status,
                markdown_table_cell(&row.detail)
            ));
        }
    }
    std::fs::write(root.join("CITATION_VALIDATION.md"), text)
        .with_context(|| format!("write {}", root.join("CITATION_VALIDATION.md")))?;
    Ok(())
}

fn ensure_citation_validation_passed(report: &CitationValidationReport) -> Result<()> {
    if report.status == "failed" {
        let missing = report
            .rows
            .iter()
            .filter(|row| row.status != "ok")
            .map(|row| format!("{} ({})", row.citation, row.detail))
            .collect::<Vec<_>>()
            .join("; ");
        anyhow::bail!("generated packet has unresolved seed citations: {missing}");
    }
    Ok(())
}

fn markdown_table_cell(input: &str) -> String {
    input.replace('|', "\\|").replace('\n', "<br>")
}

fn select_packet_slice(source: &Utf8Path, limit: usize) -> Result<PacketSlice> {
    let mut slice = PacketSlice {
        feature: "indexing".to_string(),
        files: Vec::new(),
        citations: Vec::new(),
        blockers: source_map_blockers(source)?,
    };
    let mut files = BTreeSet::<Utf8PathBuf>::new();
    let mut citations = BTreeSet::<String>::new();
    for table in [
        "feature_tags",
        "symbols",
        "call_edges",
        "dataflow_edges",
        "equivalence_edges",
        "equivalence_diffs",
    ] {
        for row in read_fact_rows(source, table)? {
            if !row_mentions_feature(&row, "indexing") {
                continue;
            }
            if let Some(citation) = fact_citation(table, &row) {
                citations.insert(citation);
            }
            for file in source_files_from_fact(source, &row) {
                files.insert(file);
            }
        }
    }
    if files.is_empty() {
        slice.feature = "source-map".to_string();
        files.extend(first_source_files(source, limit)?);
    }
    slice.files = files.into_iter().take(limit).collect();
    slice.citations = citations.into_iter().take(12).collect();
    Ok(slice)
}

fn read_fact_rows(source: &Utf8Path, table: &str) -> Result<Vec<serde_json::Value>> {
    let path = source.join(format!(".c2rust-port/knowledge/facts/{table}.jsonl"));
    if !path.exists() {
        return Ok(Vec::new());
    }
    let text = std::fs::read_to_string(&path).with_context(|| format!("read {path}"))?;
    let mut rows = Vec::new();
    for (index, line) in text.lines().enumerate() {
        if line.trim().is_empty() {
            continue;
        }
        rows.push(
            serde_json::from_str(line)
                .with_context(|| format!("parse {path} line {}", index + 1))?,
        );
    }
    Ok(rows)
}

fn source_map_blockers(source: &Utf8Path) -> Result<Vec<String>> {
    let mut blockers = Vec::new();
    for row in read_fact_rows(source, "capabilities")? {
        let name = row
            .get("name")
            .and_then(|value| value.as_str())
            .unwrap_or("");
        let status = row
            .get("status")
            .and_then(|value| value.as_str())
            .unwrap_or("");
        if !matches!(name, "codeql" | "doxygen") || status != "ran_failed" {
            continue;
        }
        let blocker_text = row
            .get("blockers")
            .and_then(|value| value.as_array())
            .into_iter()
            .flatten()
            .filter_map(|value| value.as_str())
            .collect::<Vec<_>>()
            .join("; ");
        blockers.push(if blocker_text.is_empty() {
            format!("`{name}` failed; inspect capability and tool-run facts.")
        } else {
            format!("`{name}` failed: {blocker_text}")
        });
    }
    Ok(blockers)
}

fn row_mentions_feature(row: &serde_json::Value, feature: &str) -> bool {
    if row
        .get("feature")
        .and_then(|value| value.as_str())
        .is_some_and(|value| value == feature)
    {
        return true;
    }
    let text = serde_json::to_string(row)
        .unwrap_or_default()
        .to_ascii_lowercase();
    match feature {
        "indexing" => [
            "index",
            "idx",
            "bwt",
            "ebwt",
            "fm_index",
            "fm-index",
            "suffix",
            "sais",
            "blockwise",
        ]
        .iter()
        .any(|needle| text.contains(needle)),
        _ => text.contains(feature),
    }
}

fn fact_citation(table: &str, row: &serde_json::Value) -> Option<String> {
    row.get("key")
        .and_then(|value| value.as_str())
        .map(|key| format!("facts/{table}.jsonl:{key}"))
}

fn source_files_from_fact(source: &Utf8Path, row: &serde_json::Value) -> Vec<Utf8PathBuf> {
    let mut files = Vec::new();
    for key in [
        "path",
        "cpp_path",
        "source",
        "from",
        "caller",
        "entity",
        "cpp_entity",
    ] {
        if let Some(value) = row.get(key).and_then(|value| value.as_str()) {
            if let Some(path) = normalize_source_file_path(source, value) {
                files.push(path);
            }
        }
    }
    files
}

fn normalize_source_file_path(source: &Utf8Path, value: &str) -> Option<Utf8PathBuf> {
    let path = Utf8Path::new(value);
    let relative = if path.is_absolute() {
        path.strip_prefix(source).ok()?.to_path_buf()
    } else {
        path.to_path_buf()
    };
    is_c_or_cpp_file(&relative).then_some(relative)
}

fn first_source_files(source: &Utf8Path, limit: usize) -> Result<Vec<Utf8PathBuf>> {
    let mut files = Vec::new();
    for entry in WalkDir::new(source).into_iter().filter_map(Result::ok) {
        if !entry.file_type().is_file() {
            continue;
        }
        let path = Utf8PathBuf::from_path_buf(entry.path().to_path_buf())
            .map_err(|p| anyhow::anyhow!("non-utf8 path: {}", p.display()))?;
        if is_c_or_cpp_file(&path) {
            files.push(path.strip_prefix(source).unwrap_or(&path).to_path_buf());
        }
        if files.len() >= limit {
            break;
        }
    }
    Ok(files)
}

fn is_c_or_cpp_file(path: &Utf8Path) -> bool {
    matches!(
        path.extension(),
        Some("c" | "cc" | "cpp" | "cxx" | "C" | "h" | "hh" | "hpp" | "hxx")
    )
}

