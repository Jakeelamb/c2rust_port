#[derive(Debug, Serialize)]
struct PacketOutcome {
    packet: String,
    status: String,
    notes: String,
}

#[derive(Debug, Serialize)]
struct CitationValidationReport {
    status: String,
    rows: Vec<CitationValidationRow>,
}

#[derive(Debug, Serialize)]
struct CitationValidationRow {
    citation: String,
    status: String,
    detail: String,
}

#[derive(Debug, Default)]
struct PacketSlice {
    feature: String,
    files: Vec<Utf8PathBuf>,
    citations: Vec<String>,
    blockers: Vec<String>,
}

pub fn run(source: &Utf8Path, target: &Utf8Path) -> Result<()> {
    let root = target.join(".c-to-rust-port");
    std::fs::create_dir_all(root.join("units/000-source-map"))?;
    std::fs::create_dir_all(root.join("prompt_profiles"))?;
    std::fs::create_dir_all(root.join("agents"))?;
    std::fs::create_dir_all(root.join("codex_runs"))?;
    let profile = PacketProfile::default();
    profile.write_to(&root.join("prompt_profiles/translator-default.toml"))?;
    copy_evidence_queries(source, &root)?;
    let slice = select_packet_slice(source, 8)?;
    let validation = validate_seed_citations(source, &slice.citations)?;
    write_citation_validation(&root, &validation)?;
    ensure_citation_validation_passed(&validation)?;
    write_packet(
        source,
        target,
        &root.join("units/000-source-map/TASK.md"),
        &slice,
    )?;
    write_codex_agent_docs(&root.join("agents"))?;
    write_codex_review_checklist(&root.join("agents/REVIEW_CHECKLIST.md"))?;
    write_codex_runbook(&root.join("agents/RUNBOOK.md"), &profile)?;
    let outcome = PacketOutcome {
        packet: "000-source-map".to_string(),
        status: "planned".to_string(),
        notes: "generated by c2rust-port v1".to_string(),
    };
    std::fs::write(
        root.join("packet_outcomes.jsonl"),
        format!("{}\n", serde_json::to_string(&outcome)?),
    )?;
    println!("wrote packet plan under {root}");
    Ok(())
}

fn write_packet(
    source: &Utf8Path,
    target: &Utf8Path,
    path: &Utf8Path,
    slice: &PacketSlice,
) -> Result<()> {
    let mut text = String::new();
    text.push_str("# TASK: Source Map Bootstrap\n\n");
    text.push_str("## Scope\n");
    text.push_str(&format!(
        "Draft a narrow Rust porting plan for the `{}` source slice only. Preserve the process and data-flow boundaries from the generated repo map.\n\n",
        slice.feature
    ));
    text.push_str("## Required Context\n");
    text.push_str(
        "- Read `.c-to-rust-port/SOURCE_REPO_MAP.md` for source process flow and data flow.\n",
    );
    text.push_str(
        "- Read `.c-to-rust-port/RUST_MIRROR_PLAN.md` for the initial Rust module layout.\n",
    );
    text.push_str(&format!(
        "- Query source knowledge at `{}/.c2rust-port/knowledge/evidence.db` before proposing code.\n",
        source
    ));
    text.push_str(&format!(
        "- Use source fact rows from `{}/.c2rust-port/knowledge/facts/*.jsonl` for citations.\n",
        source
    ));
    text.push_str(
        "- Read `.c-to-rust-port/EVIDENCE_QUERIES.md` for SQL recipes against `evidence.db`.\n",
    );
    text.push_str(
        "- Read `.c-to-rust-port/CITATION_VALIDATION.md` for generated seed-citation validation.\n",
    );
    text.push_str("- Do not invent a different module structure unless the map evidence is wrong and you document why.\n\n");
    if !slice.blockers.is_empty() {
        text.push_str("## Source Map Blockers\n");
        text.push_str("Source map incomplete for this packet until these deep-analysis blockers are addressed or explicitly judged irrelevant:\n");
        for blocker in &slice.blockers {
            text.push_str(&format!("- {blocker}\n"));
        }
        text.push('\n');
    }
    text.push_str("## Required Source Evidence\n");
    text.push_str("- Cite at least three source fact rows before the unified diff.\n");
    text.push_str("- Use citation format `facts/<table>.jsonl:<key>` or `evidence.db:<table>:<primary-key>`.\n");
    text.push_str("- At least one citation must come from `symbols`, `call_edges`, `dataflow_edges`, `feature_tags`, or `equivalence_edges`.\n");
    text.push_str("- If citations are unavailable, return a blocker instead of a patch.\n\n");
    if !slice.citations.is_empty() {
        text.push_str("## Seed Citations\n");
        for citation in &slice.citations {
            text.push_str(&format!("- `{citation}`\n"));
        }
        text.push('\n');
    }
    text.push_str("## Source Slice\n");
    for file in &slice.files {
        text.push_str(&format!("- `{file}`\n"));
    }
    text.push_str("\n## Target Rust Files\n");
    text.push_str(&format!("- `{}`\n", target.join("src/lib.rs")));
    text.push_str("- Add files only if the apply/converge packet explicitly allows it.\n\n");
    text.push_str("## Expected Behavior\n");
    text.push_str("Preserve observable behavior from the source slice. If behavior is unclear, write questions and evidence instead of guessing.\n\n");
    text.push_str("## Translator Restrictions\n");
    text.push_str("- Do not run git.\n");
    text.push_str("- Do not run Cargo, builds, benchmarks, package managers, or broad scans.\n");
    text.push_str("- Do not edit the shared worktree.\n");
    text.push_str("- Produce a draft patch and notes only.\n");
    text.push_str("- Use only the context in this packet and the named target file excerpts.\n");
    text.push_str("- Every behavior claim must be backed by a source fact citation.\n");
    text.push_str("- If context is insufficient, return the smallest useful skeleton and list the missing excerpts.\n\n");
    text.push_str("## Patch Contract\n");
    text.push_str(
        "- Output `Source Evidence`, then one unified diff, then an `Assumptions` section.\n",
    );
    text.push_str("- Do not mark an existing file as `new file mode`.\n");
    text.push_str(
        "- Do not add modules unless the target file list explicitly names the module file.\n",
    );
    text.push_str(
        "- Do not remove existing public items unless the task explicitly asks for removal.\n",
    );
    text.push_str("- Do not use crates that are not already in `Cargo.toml`.\n");
    text.push_str(
        "- Preserve source enum order, integer discriminants, and public naming evidence.\n",
    );
    text.push_str(
        "- Prefer allocation-free constants/slices over maps unless keyed lookup is required.\n\n",
    );
    text.push_str("## Allowed Verification Command\n");
    text.push_str("None for translator packets. Apply/converge packets own verification.\n");
    std::fs::write(path, text).with_context(|| format!("write {path}"))
}

